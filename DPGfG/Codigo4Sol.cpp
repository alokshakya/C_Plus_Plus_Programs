const LL MOD = 1e9 + 7;
LL dp[6][6][26];
int vis[6][6][26];
LL sum[26],birth[26];
LL call(int k,int l,int n)
{
    if(vis[k][l][n])return dp[k][l][n];
    vis[k][l][n] = 1;
    birth[1] = 1;
    sum[1] = 1;
    for(int i = 2;i<=n;i++){
        LL c = 0;
        if(i - l >=1)c = - birth[i - l];
        birth[i] = ((sum[i-1] + c)*k)%MOD;
        sum[i] = (sum[i-1] + birth[i] + c) % MOD;
    }
    return dp[k][l][n] = (sum[n] + MOD ) %MOD ;
}
